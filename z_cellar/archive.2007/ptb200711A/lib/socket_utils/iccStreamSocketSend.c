INT32 iccStreamSocketSend(INT16 sockFD, VOID *msg, INT32 msgSize)
{
  INT32 numBytes;

  numBytes = send(sockFD, msg, msgSize, 0);

  if(numBytes == -1)
  {
    XMC_ERROR_STDOUT("ERROR SENDING DATA OVER SOCKET");
    perror("iccStreamSocketSend: send");
    return(-1);
  }
  else if (numBytes != msgSize)
  {
    XMC_ERROR_STDOUT("RETURNED NUMBER OF BYTES NOT SAME AS MSGSIZE");
    return(-1);
  }
  else return(numBytes);
}

