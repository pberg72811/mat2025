FLT64 xccRa2Lon( FLT64 ra, FLT64 JS )
{
  FLT64 lon;
  
  lon = ra - xccGetPhiGrn( JS );
  
  lon = fmod(lon,TWOPI);
  if( lon >  PI ) lon = lon - TWOPI;
  if( lon < -PI ) lon = lon + TWOPI;

  return lon;
}
