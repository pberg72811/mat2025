VOID xccDirCos2AzEl(FLT64 alpha, FLT64 beta, FLT64 gamma, FLT64 *az, FLT64 *el)
{

// Calculate off-boresight and azimuth.
//------------------------------------------------------------------------------
  *el = acos(gamma);
  *az = atan2(beta,alpha);
  return;
}

VOID xccAzEl2DirCos(FLT64 az, FLT64 el, FLT64 *alpha, FLT64 *beta, FLT64 *gamma)
{

// Calculate alpha, beta, and gamma.
//------------------------------------------------------------------------------
  *alpha = sin(el)*cos(az);
  *beta  = sin(el)*sin(az);
  *gamma = cos(el);
  return;
}
