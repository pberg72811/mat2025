UNT16 xccMARCode2SeedI( INT32 userCodeIndex )
{
//COMMON-CODE RegisterA START.
  // Make sure userCodeIndex is within range.
  //------------------------------------------------------------------------------
  if( userCodeIndex > MAX_USER_CODE_INDEX )
  {
    userCodeIndex = MAX_USER_CODE_INDEX;
    XMC_ERROR_STDOUT("userCodeIndex > MAX_USER_CODE_INDEX" );
  }

  if( userCodeIndex < MIN_USER_CODE_INDEX )
  {
    userCodeIndex = MIN_USER_CODE_INDEX;
    XMC_ERROR_STDOUT("userCodeIndex < MAX_USER_CODE_INDEX" );
  }

  // Make sure userCodeIndex is within range.
  //------------------------------------------------------------------------------
  return ( xccUserCodeAssignmentRegisterA[userCodeIndex] );
//COMMON-CODE RegisterA  STOP.
}

