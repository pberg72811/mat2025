typedef struct
{
  INT08 x1;
  INT08 x2;
}endianINT16_t;

typedef struct
{
  INT08 x1;
  INT08 x2;
  INT08 x3;
  INT08 x4;
}endianINT32_t;

typedef struct
{
  INT08 x1;
  INT08 x2;
  INT08 x3;
  INT08 x4;
  INT08 x5;
  INT08 x6;
  INT08 x7;
  INT08 x8;
}endianINT64_t;

/* Macros -------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------------*/
/* Prototypes ---------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------------*/
/* Routines -----------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------------*/
VOID xccvSwapEndianINT16(VOID *a, INT32 i, VOID *b, INT32 j, UNT32 n)
{
  INT32 ii;
  endianINT16_t *pa=(endianINT16_t*)a;
  endianINT16_t *pb=(endianINT16_t*)b;

  for(ii=0;ii<n;ii++)
  {
    pb->x1 = pa->x2;
    pb->x2 = pa->x1;
    pa+=i;
    pb+=j;
  }
}

VOID xccvSwapEndianINT32(VOID *a, INT32 i, VOID *b, INT32 j, UNT32 n)
{
  INT32 ii;
  endianINT32_t *pa=(endianINT32_t*)a;
  endianINT32_t *pb=(endianINT32_t*)b;

  for(ii=0;ii<n;ii++)
  {
    pb->x1 = pa->x4;
    pb->x2 = pa->x3;
    pb->x3 = pa->x2;
    pb->x4 = pa->x1;
    pa+=i;
    pb+=j;
  }
}

VOID xccvSwapEndianINT64(VOID *a, INT32 i, VOID *b, INT32 j, UNT32 n)
{
  INT32 ii;
  endianINT64_t *pa=(endianINT64_t*)a;
  endianINT64_t *pb=(endianINT64_t*)b;

  for(ii=0;ii<n;ii++)
  {
    pb->x1 = pa->x8;
    pb->x2 = pa->x7;
    pb->x3 = pa->x6;
    pb->x4 = pa->x5;
    pb->x5 = pa->x4;
    pb->x6 = pa->x3;
    pb->x7 = pa->x2;
    pb->x8 = pa->x1;

    pa+=i;
    pb+=j;
  }
}

