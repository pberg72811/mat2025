int main(int argc, char **argv)
{
  INT32 ii;
  INT32 amountRead;
  INT08 inDataINT08[BLOCK_SIZE];

  // Handle input parameters.
  //----------------------------------------------------------------------------
  for(ii = 0; ii<argc; ii++)
  {
    if( (!strncmp( argv[ii], "-h",     2)) || 
	(!strncmp( argv[ii], "--help", 6))   )
    {
      fprintf(stderr, ASCIIDUMP_FUNC_DESC);
      exit(0);
    }

  }

// Read in the data and write it out.
//------------------------------------------------------------------------------
  while ( (amountRead = fread(inDataINT08, sizeof(inDataINT08[0]), BLOCK_SIZE, stdin)) == BLOCK_SIZE )
  {
    for(ii=0; ii<amountRead; ii++)
    {
      if     (inDataINT08[ii]>96 && inDataINT08[ii]<123) printf("%c",inDataINT08[ii]-32);
      else                                               printf("%c",inDataINT08[ii]);
    }
  }
  for(ii=0; ii<amountRead; ii++)
  {
      if     (inDataINT08[ii]>96 && inDataINT08[ii]<123) printf("%c",inDataINT08[ii]-32);
      else                                               printf("%c",inDataINT08[ii]);
  }

  return(0);
}

