function quadSpline(x,y)
A = zeros(9);

A(1,1) = 1; A(1,2) = x(1); A(1,3) = x(1) *x(1);
A(2,1) = 1; A(2,2) = x(2); A(2,3) = x(2) *x(2);

A(3,4) = 1; A(3,5) = x(2); A(3,6) = x(2) *x(2);
A(4,4) = 1; A(4,5) = x(3); A(4,6) = x(3) *x(3);

A(5,7) = 1; A(5,8) = x(3); A(5,9) = x(3) *x(3);
A(6,7) = 1; A(6,8) = x(4); A(6,9) = x(4) *x(4);

A(7,2) = 1; A(7,3) = 2*x(1); A(7,5) = -1; A(7,6) = -2*x(1);

A(8,5) = 1; A(8,6) = 2*x(2); A(8,8) = -1; A(8,9) = -2*x(2);

A(9,3) = 1;

b = [y(1);y(2);y(2);y(3);y(3);y(4);0;0;0];

disp(A);
disp(b);
disp(inv(A))
disp(inv(A) * b);
disp(A/b);


end
